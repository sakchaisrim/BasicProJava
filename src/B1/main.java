package B1;

public class main {
    public static void main(String[] args) {
        // Casting Variable
        long l = 9999L;
        int i = (int) l;
        double d = 123.256;
        int i1 = (int) d;
        char c = 'A';
        int i2 = (int) c;
        System.out.printf("i= "+ i);
        System.out.printf("\ni= "+ i1);
        System.out.printf("\ni= "+ i2);

    }
}
